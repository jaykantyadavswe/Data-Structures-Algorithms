package DSA.Hashing;
import java.util.*;
public class LinkedHashSets {
    public static void main(String[] args) {
        LinkedHashSet<String> lhs = new LinkedHashSet();
        lhs.add("Delhi");
        lhs.add("Mumbai");
        lhs.add("Noida");
        lhs.add("Bengaluru");

        System.out.println(lhs);

        // All Operations Are same
    }
}
